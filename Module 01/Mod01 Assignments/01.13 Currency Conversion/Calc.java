public class Calc {
    public static void main(String[] args) {

        int value = 7;
        // calculations and output statements

        System.out.println("The original value is " + value);
        System.out.println("When " + value + " is devided by 2, the whole number result is " + (value / 2));
        System.out.println("The remainder after the number " + value + " is devided by 2 is " + (value % 2));
        System.out.println("When " + value + " is devided by 2, the decimal number result is " + ((double) value / 2));

    }
}